    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String nm = jTextField5.getText().trim();
        String cellNo = jTextField7.getText().trim();
        String schName = jTextField6.getText().trim();
        int schGrade = jComboBox1.getSelectedIndex();
        
        String sbj = "";
        if (jCheckBox1.isSelected()) {
            sbj += "1";
        } else {
            sbj += "0";
        }
        if (jCheckBox2.isSelected()) {
            sbj += "1";
        } else {
            sbj += "0";
        }
        if (jCheckBox3.isSelected()) {
            sbj += "1";
        } else {
            sbj += "0";
        }
        if (jCheckBox4.isSelected()) {
            sbj += "1";
        } else {
            sbj += "0";
        }
        
        int cPackage = 0;
        if (jComboBox2.getSelectedIndex() == 0) {
            cPackage = 1;
        } else if (jComboBox2.getSelectedIndex() == 1) {
            cPackage = 3;
        } else if (jComboBox2.getSelectedIndex() == 2) {
            cPackage = 5;
        }
        
        StudentDB stuDB = new StudentDB();
        stuDB.UpdateData(stuDB.SearchForFile(studentID), new Student(studentID, nm, cellNo, sbj, schName, schGrade, cPackage, DBController.studentDB.get(idx).getTotalLessons()));
    }//GEN-LAST:event_jButton4ActionPerformed

